using System;
using System.Collections.Generic;
using System.Text;


public class Stats
{
    private int endurance;
    private int sprint;
    private int dribble;
    private int passing;
    private int shooting;

    public int Endurance
    {
        get { return endurance; }
        set { endurance = value; }
    }

    public int Sprint
    {
        get { return sprint; }
        set { sprint = value; }
    }

    public int Dribble
    {
        get { return dribble; }
        set { dribble = value; }
    }

    public int Passing
    {
        get { return passing; }
        set { passing = value; }
    }

    public int Shooting
    {
        get { return shooting; }
        set { shooting = value; }
    }
}

